// If Else 

var num = Number(prompt("Enter the number to check whether it is Even or Odd :"))

if (num % 2 == 0) {
    document.write(`${num} is Even`);
}
else {
    document.write(`${num} is odd`)
}



// WAP to check whether a person can vote or not.

Age = Number(prompt("Enter your legal age : "))
if (Age>=18){
    document.write("You can Vote")
}
else{
    document.write("You can not Vote")
}

